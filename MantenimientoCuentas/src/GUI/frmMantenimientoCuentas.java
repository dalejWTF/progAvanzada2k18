    public void CargarTipoCta(String tipoCta) {

        ManejadorCuentas manejadorCuentas = new ManejadorCuentas();
        ArrayList<Catalogo> listaTipoCta = new ArrayList<>();

        try {
            Catalogo ct = new Catalogo(-1, "Seleccione...");
            listaTipoCta.add(ct);

            listaTipoCta.addAll(manejadorCuentas.obtenerTipoCta());

            DefaultComboBoxModel cmb = new DefaultComboBoxModel();

            int j = 0;
            for (Catalogo c : listaTipoCta) {
                if (c.getRecurso().equals(tipoCta)) {
                    j = j + 1;
                }
                cmb.addElement(c);
            }

            cmbTipoCta.setModel(cmb);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmMantenimientoCuentas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmMantenimientoCuentas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void CargarTabla(ArrayList<Cliente> cliente) {
        DefaultTableModel model = (DefaultTableModel) tblTitular.getModel();
        model.setRowCount(0);
        tblTitular = new JTable(model);
        Object rowData[] = new Object[3];
        for (int i = 0; i < cliente.size(); i++) {
            rowData[0] = cliente.get(i).getCedula();
            rowData[1] = cliente.get(i).getApellidos();
            rowData[2] = cliente.get(i).getNombres();

            model.addRow(rowData);

        }
    }

    public void SeleccionarTipo(String tipo) {
        ComboBoxModel cmb = cmbTipoCta.getModel();
        int j = -1;
        for (int i = 0; i < cmb.getSize(); i++) {
            Catalogo c = (Catalogo) cmb.getElementAt(i);

            if (c.getRecurso().equals(tipo)) {
                j = i;
            }
        }
        cmbTipoCta.setSelectedIndex(j);
    }

    public ArrayList<Cliente> ActualizarTabla(Cliente cliente) {
        ArrayList<Cliente> clientes = new ArrayList<>();
        for (int i = 0; i < c.getTitularCta().size(); i++) {
            clientes.add(c.getTitularCta().get(i));
        }
        clientes.add(cliente);
        return clientes;
    }

