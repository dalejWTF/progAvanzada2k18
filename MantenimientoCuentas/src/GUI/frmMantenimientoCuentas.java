    private void btnBuscarCuentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarCuentaMouseClicked
        // TODO add your handling code here:
        ManejadorCuentas manejadorCuentas = new ManejadorCuentas();
        try {
            c = manejadorCuentas.ObtenerCuenta(txtNroCta.getText());
            txtSaldo.setText(Double.toString(c.getSaldo()));
            txtNroCta.setText(c.getNroCta());
            String fecha = new SimpleDateFormat("yyyy-MM-dd").format(c.getFechaApertura());
            lblFechaApertura.setText(fecha);
            CargarTabla(c.getTitularCta());
            if (c.getEstadoCta().equals("Activa")) {
                rdoActiva.setSelected(true);
            } else if (c.getEstadoCta().equals("Inactiva")) {
                rdoInactiva.setSelected(true);
            }
            SeleccionarTipo(c.getTipoCta());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmMantenimientoCuentas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmMantenimientoCuentas.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnBuscarCuentaMouseClicked

