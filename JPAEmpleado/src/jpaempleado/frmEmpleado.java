    private void CrearTablaEmp() {
        Object tblCol[] = {"Id", "Cedula", "Apellidos", "Nombres", "Edad", "Sueldo"};
        model = new DefaultTableModel(null, tblCol);
        jtblEmp.setModel(model);
    }

    private void CargarInf() {
        try {
            List<Empleado> listEmp = ControladorEmp.findEmpleadoEntities();
            DefaultTableModel model = (DefaultTableModel) jtblEmp.getModel();
            model.setRowCount(0);
            jtblEmp = new JTable(model);
            Object rowData[] = new Object[6];
            for (int i = 0; i < listEmp.size(); i++) {
                rowData[0] = listEmp.get(i).getIdEmpleado();
                rowData[1] = listEmp.get(i).getCedula();
                rowData[2] = listEmp.get(i).getApellidos();
                rowData[3] = listEmp.get(i).getNombres();
                rowData[4] = listEmp.get(i).getEdad();
                rowData[5] = listEmp.get(i).getSueldo();
                model.addRow(rowData);

            }
        } catch (Exception e) {
        }

    }

    public void SeleccionarEmpleado() {

        int fila = jtblEmp.getSelectedRow();
        jtxtfCedula.setText(jtblEmp.getModel().getValueAt(fila, 1).toString());
        jtxtfApellidos.setText(jtblEmp.getModel().getValueAt(fila, 2).toString());
        jtxtfNombres.setText(jtblEmp.getModel().getValueAt(fila, 3).toString());
        jtxtfEdad.setText(jtblEmp.getModel().getValueAt(fila, 4).toString());
        jtxtfSueldo.setText(jtblEmp.getModel().getValueAt(fila, 5).toString());
    }

